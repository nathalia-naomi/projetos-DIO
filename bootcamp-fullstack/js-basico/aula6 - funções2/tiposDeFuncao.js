// tipos de função

// declarativas
function funcao() {
  console.log('Tudo certo jovem!');
}
funcao();

// expressão de função
// com nomeação
var funcao2 = function () {
  console.log('Sou uma mensangem da função de expressão');
};
funcao2();

// arrow function
var funcao3 = () => {
  console.log('Sou uma arrow function');
};
funcao3();
